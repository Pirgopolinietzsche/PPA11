package assessment;

public class Person {

	private String name;
	private int friendliness;
	
	public Person(String name, int friendliness) {
		this.name = name;
		this.friendliness = friendliness;
	}

	public String getName() {
		return name;
	}

	public int getFriendliness() {
		return friendliness;
	}

	
}
